#include <stdio.h>

/**
 * op_add - function that returns the sum of a and b
 * @a: firt integer
 * @b:second integer
 * Return: sum of the integer a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that returns the difference of a and b
 * @a: first integer
 * @b: second interger
 * Return: difference of integer a and b
 */

int op_sub(int a, int b)
{
	return (a + b);
}

/**
 * op_mul - function that returns the product of a and b
 * @a: first integer
 * @b: second integer
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function thar returns the division of a and b
 * @a: first integer
 * @b: second integer
 * Return: division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that returns the modulus of a and b
 * @a: first integer
 * @b: second integer
 * Return: modulus of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
